/**
 * Render a category icon by key, falling back to inline text.
 */
function CAT_ICON(key, size = 16) {
  if (ICON_PATHS[key]) return ICON(key, size);
  return key || '';
}

/**
 * Get Lucide-style SVG markup.
 * @param {string} name - key from ICON_PATHS
 * @param {number} size - width/height in px (default 18)
 * @param {string} extraClass - optional class(es)
 * @returns {string} SVG markup
 */
function ICON(name, size = 18, extraClass = '') {
  const paths = ICON_PATHS[name] || '';
  const cls = extraClass ? ` class="lucide ${extraClass}"` : ' class="lucide"';
  return `<svg${cls} xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${paths}</svg>`;
}

// Populate any element with [data-icon="name"] on load
function hydrateIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach(el => {
    const name = el.getAttribute('data-icon');
    const size = parseInt(el.getAttribute('data-icon-size') || '18', 10);
    if (ICON_PATHS[name]) {
      el.innerHTML = ICON(name, size);
    }
  });
}

if (typeof window !== 'undefined') {
  window.ICON = ICON;
  window.CAT_ICON = CAT_ICON;
  window.hydrateIcons = hydrateIcons;
  document.addEventListener('DOMContentLoaded', () => hydrateIcons());
}
